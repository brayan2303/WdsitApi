package net.woden.wdsit.model;

public class InvCyclicLayoutModel {
    private int pallets;
    private int serials;
    private int accesories;
    private int sapCodes;
    private int locations;

    public int getPallets() {
        return pallets;
    }

    public void setPallets(int pallets) {
        this.pallets = pallets;
    }

    public int getSerials() {
        return serials;
    }

    public void setSerials(int serials) {
        this.serials = serials;
    }

    public int getAccesories() {
        return accesories;
    }

    public void setAccesories(int accesories) {
        this.accesories = accesories;
    }

    public int getSapCodes() {
        return sapCodes;
    }

    public void setSapCodes(int sapCodes) {
        this.sapCodes = sapCodes;
    }

    public int getLocations() {
        return locations;
    }

    public void setLocations(int locations) {
        this.locations = locations;
    }
}
